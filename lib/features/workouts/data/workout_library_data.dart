enum WorkoutLibraryCategoryGroup {
  bodyFocused,
  goalBased,
  conditionBased,
  abilityBased,
  timeBased,
  equipment,
}

extension WorkoutLibraryCategoryGroupLabel on WorkoutLibraryCategoryGroup {
  String get displayName {
    switch (this) {
      case WorkoutLibraryCategoryGroup.bodyFocused:
        return 'Body Focused';
      case WorkoutLibraryCategoryGroup.goalBased:
        return 'Goal Based';
      case WorkoutLibraryCategoryGroup.conditionBased:
        return 'Condition-Based';
      case WorkoutLibraryCategoryGroup.abilityBased:
        return 'Ability-Based';
      case WorkoutLibraryCategoryGroup.timeBased:
        return 'Time Based';
      case WorkoutLibraryCategoryGroup.equipment:
        return 'Equipment';
    }
  }
}

class WorkoutLibraryCategory {
