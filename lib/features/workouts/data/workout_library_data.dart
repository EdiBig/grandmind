  final WorkoutConditionTag? conditionTag;
  final WorkoutAbilityTag? abilityTag;
  final WorkoutAccessibilityTag? accessibilityTag;
  final WorkoutDurationRange? durationRange;
  final WorkoutEquipment? equipment;
}

